#!/usr/bin/env node
import { spawn } from "node:child_process";
import { mkdir, rm, readdir, writeFile } from "node:fs/promises";
import { existsSync } from "node:fs";
import path from "node:path";
import { fileURLToPath } from "node:url";
import sharp from "sharp";

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const ROOT = path.resolve(__dirname, "..");

const SOURCE = path.join(ROOT, "raw_assets/Scroll_Scrub.mp4");
const FPS = 36;
const WIDTHS = [1080, 1600, 2000];
const OUT_ROOT = path.join(ROOT, "public/media/rx-brain/frames");
const TMP_ROOT = path.join(OUT_ROOT, "_tmp");
const MANIFEST_PATH = path.join(ROOT, "src/data/rx-brain-frames.ts");

function run(cmd, args) {
  return new Promise((resolve, reject) => {
    const p = spawn(cmd, args, { stdio: ["ignore", "inherit", "inherit"] });
    p.on("error", reject);
    p.on("exit", (code) => (code === 0 ? resolve() : reject(new Error(`${cmd} exited ${code}`))));
  });
}

async function extractPngs(width) {
  const tmpDir = path.join(TMP_ROOT, String(width));
  await rm(tmpDir, { recursive: true, force: true });
  await mkdir(tmpDir, { recursive: true });
  const pattern = path.join(tmpDir, "%03d.png");
  await run("ffmpeg", [
    "-v", "error",
    "-i", SOURCE,
    "-vf", `fps=${FPS},scale=${width}:-2:flags=lanczos`,
    "-q:v", "2",
    "-y",
    pattern,
  ]);
  return tmpDir;
}

async function pngsToWebp(tmpDir, width) {
  const outDir = path.join(OUT_ROOT, String(width));
  await rm(outDir, { recursive: true, force: true });
  await mkdir(outDir, { recursive: true });
  const files = (await readdir(tmpDir)).filter((f) => f.endsWith(".png")).sort();
  await Promise.all(
    files.map(async (name, i) => {
      const idx = String(i).padStart(3, "0");
      await sharp(path.join(tmpDir, name))
        .webp({ quality: 82, effort: 6 })
        .toFile(path.join(outDir, `${idx}.webp`));
    }),
  );
  return files.length;
}

async function writeManifest(count) {
  const content = `// Generated by scripts/build-rx-brain-frames.mjs — do not edit by hand.
export const rxBrainFrames = {
  widths: [${WIDTHS.join(", ")}] as const,
  count: ${count},
  path: (width: number, index: number) =>
    \`/media/rx-brain/frames/\${width}/\${String(index).padStart(3, "0")}.webp\`,
} as const;

export type RxBrainFramesManifest = typeof rxBrainFrames;
`;
  await writeFile(MANIFEST_PATH, content);
}

async function main() {
  if (!existsSync(SOURCE)) {
    console.error(`Source video not found: ${SOURCE}`);
    process.exit(1);
  }
  console.log(`Building rx-brain frames from full source @ ${FPS}fps`);
  let count = 0;
  for (const width of WIDTHS) {
    const t0 = Date.now();
    const tmpDir = await extractPngs(width);
    const n = await pngsToWebp(tmpDir, width);
    count = n;
    console.log(`  ${width}w: ${n} frames in ${Math.round((Date.now() - t0) / 1000)}s`);
  }
  await rm(TMP_ROOT, { recursive: true, force: true });
  await writeManifest(count);
  console.log(`Wrote manifest: ${path.relative(ROOT, MANIFEST_PATH)} (count=${count})`);
}

main().catch((err) => {
  console.error(err);
  process.exit(1);
});
